public class IfElseLadder {
    public static void main(String[] args) {
        int age = 18;

        if (age > 18) {
            System.out.println("You are young");
        } else if (age == 18) {
            System.out.println("YOU ARE YOUTH");
        } else {
            System.out.println("You are old");
        }
    }
}